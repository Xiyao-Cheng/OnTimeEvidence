CREATE TABLE CUSTOMER(CUSTOMER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,CUSTOMER_NAME varchar(200),CONTACT_NUMBER BIGINT,address varchar(200),gender varchar(100));

CREATE TABLE VENDOR(VENDOR_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,VENDOR_NAME varchar(200),VENDOR_CONTACT_NO BIGINT,VENDOR_EMAIL varchar(200),VENDOR_USER_NAME varchar(200),VENDOR_ADDRESS varchar(200));

CREATE TABLE INVENTORY(SKU_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,PRODUCT_NAME varchar(200), PRODUCT_LABEL VARCHAR(200),INVENTORY_ON_HAND INTEGER,MIN_QTY_REQ INTEGER,PRICE DOUBLE);

CREATE TABLE ORDERS(ORDER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,CUSTOMER_ID BIGINT, PAYMENT_CHANNEL VARCHAR(200),IS_COD BOOLEAN,ORDER_STATUS VARCHAR(200), ORDER_CREATED_ON BIGINT, TOTAL_AMOUNT DOUBLE, SHIPPING_ADDRESS VARCHAR(200), FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID));


CREATE TABLE ORDER_LINE_ITEM(ORDER_LINE_ITEM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,SKU_ID BIGINT, ORDER_ID BIGINT,ITEM_QTY INT, FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID));




